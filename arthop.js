function arthop(num1, num2, operator) {
    switch (operator) {
        case '+':
            console.log('Sum : ' + (num1 + num2));
            break;
        case '-':
            console.log('Sub : ' + (num1 - num2));
            break;
        case '*':
            console.log('Mul : ' + (num1 * num2));
            break;
        case '/':
            console.log('Div : ' + (num1 / num2));
            break;
    }
}
